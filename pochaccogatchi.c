enum State{
  EGG,
  HATCHING,
  POCHACCO
};
//Set first state
State currentState = EGG;
int eggClicks = 0;
bool lastButtonState = LOW; 

void setup() {
  Serial.begin(115200);
  display.setTextSize(1);      
  display.setTextColor(WHITE);
  pinMode(4, INPUT_PULLUP);
  pinMode(5, INPUT_PULLUP);
  pinMode(16, INPUT_PULLUP);

  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) { // Address 0x3D for 128x64
    Serial.println(("SSD1306 allocation failed"));
    for(;;);
  }
  delay(2000);
  display.clearDisplay();

  syncTime();

}

void loop() {
bool buttonPress = digitalRead(4);

//EGG to POCHACCO pipeline
  switch(currentState){
     case EGG:
      display.setCursor(20, 20);
      //display.println("IT'S AN EGG!");
      //delay(50);
      //Issues here with delay
      display.clearDisplay();
      display.drawBitmap(0, 0, eggArray, 128, 64, 1);
        display.display();
      if (buttonPress == LOW && lastButtonState == HIGH) {
           eggClicks++;
           if (eggClicks >= 5){
             currentState = HATCHING;
           }
        }
      break;
     case HATCHING:
        display.clearDisplay();
        display.setCursor(20,20);
        display.println("IT's HATCHING...");
        display.display();
        delay(200);
        display.clearDisplay();
        delay(2000); //pause
        currentState = POCHACCO;
         break;
     case POCHACCO:
        display.drawBitmap(0, 0, image_data_Pochaccoarray, 128, 64, 1);
        display.display();
        break;
  }
    lastButtonState = buttonPress; // Update the state for the next loop
    display.display(); // Push everything to the OLED
}
